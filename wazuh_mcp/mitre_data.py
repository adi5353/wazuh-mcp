def enrich_mitre_ids(technique_ids: list[str]) -> list[dict]:
    """Resolve a list of MITRE technique IDs to name + tactic."""
    enriched = []
    for tid in technique_ids:
        base_id = tid.split(".")[0]
        info = _MITRE_MAP.get(base_id, {})
        enriched.append({
            "id": tid,
            "name": info.get("name", "Unknown Technique"),
            "tactic": info.get("tactic", "Unknown"),
        })
    return enriched


def get_technique(technique_id: str) -> dict:
    """Return name+tactic for a single technique ID, or empty dict if unknown."""
    base_id = technique_id.split(".")[0]
    return _MITRE_MAP.get(base_id, {})
